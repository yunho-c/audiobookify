import 'package:flutter/material.dart';
import 'package:google_fonts/google_fonts.dart';

class AppTheme {
  // Colors
  static const Color background = Color(
    0xFF0A0F1A,
  ); // dark slate instead of pure black
  static const Color surface = Color(0xFF0F172A); // slate-900
  static const Color surfaceLight = Color(0xFF1E293B); // slate-800
  static const Color accent = Color(0xFFF97316); // orange-500
  static const Color accentLight = Color(0xFFFB923C); // orange-400
  static const Color textPrimary = Color(0xFFFFFFFF);
  static const Color textSecondary = Color(0x99FFFFFF); // white/60
  static const Color textTertiary = Color(0x66FFFFFF); // white/40
